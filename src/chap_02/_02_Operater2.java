package chap_02;

public class _02_Operater2 {
    public static void main(String[] args) {
        // 대입 연산자
        int num = 10;
        num += 2;
        System.out.println(num); // 12

        num = 10;
        num -= 2;
        System.out.println(num); // 8

        num = 10;
        num *= 2;
        System.out.println(num); //20

        num = 10;
        num /= 2;
        System.out.println(num); //5 몫

        num = 10;
        num %= 2;
        System.out.println(num); //0 나머지
    }
}
